def grayToBinary(num):
        mask = num >> 1
        while (mask != 0) :  
                num = num ^ mask;
                mask = mask >> 1
        return num

def parity(spp, parbit):
    spar = int(spp[0])
    for i in range(1,30):
        spar = int(spp[i]) ^ spar #even parity
    if spar == parbit : 
        return True
    return False

